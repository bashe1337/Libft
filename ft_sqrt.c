#include "libft.h"

int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	while ((i * i) <= nb)
	{
		i++;
	}
	if (((i - 1) * (i - 1)) == nb)
	{
		return (i - 1);
	}
	else
	{
		return (0);
	}
}
