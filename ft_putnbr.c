#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;
	char	str[11];
	int		m;

	i = 0;
	m = 1;
	if (n < 0)
		m = -1;
	while (n != 0)
	{
		str[i] = n % 10 + '0';
		n = n / 10;
		i++;
	}
	if (m < 0)
		str[i] = '-';
	if (i > 1)
		i = i - 1;
	while (i >= 0)
	{
		ft_putchar(str[i]);
		i--;
	}
}
